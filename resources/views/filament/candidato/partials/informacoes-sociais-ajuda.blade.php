<div class="space-y-6 text-sm text-gray-700">
    <div>
        <h3 class="font-semibold text-gray-900">Identidade de Gênero</h3>
        <ul class="list-disc list-inside mt-2 space-y-1">
            <li><strong>Cisgênero:</strong> pessoa que se identifica com o gênero atribuído ao nascer.</li>
            <li><strong>Transgênero:</strong> pessoa que se identifica com gênero diferente do atribuído ao nascer.</li>
            <li><strong>Não-binário:</strong> pessoa que não se identifica exclusivamente como homem ou mulher.</li>
            <li><strong>Travesti:</strong> identidade de gênero feminina, não reduzida a homem/mulher.</li>
            <li><strong>Outro:</strong> nenhuma das anteriores.</li>
        </ul>
    </div>

    <div>
        <h3 class="font-semibold text-gray-900">Nome Social</h3>
        <p class="mt-2">
            É o nome pelo qual a pessoa prefere ser identificada em sociedade, diferente do seu nome civil.
        </p>
    </div>

    <div>
        <h3 class="font-semibold text-gray-900">Orientação Sexual</h3>
        <ul class="list-disc list-inside mt-2 space-y-1">
            <li><strong>Heterossexual:</strong> atração pelo gênero oposto.</li>
            <li><strong>Homossexual:</strong> atração pelo mesmo gênero.</li>
            <li><strong>Bissexual:</strong> atração por ambos os gêneros.</li>
            <li><strong>Panssexual:</strong> atração por todas as identidades de gênero.</li>
            <li><strong>Assexual:</strong> ausência de atração sexual.</li>
        </ul>
    </div>

    <div>
        <h3 class="font-semibold text-gray-900">Pessoa com Deficiência</h3>
        <p class="mt-2">
            Inclui deficiência física, sensorial, intelectual, transtorno global do desenvolvimento,
            altas habilidades ou superdotação.
        </p>
    </div>

    <div>
        <h3 class="font-semibold text-gray-900">Raça/Cor</h3>
        <p class="mt-2">
            Classificação segundo critérios do IBGE: preta, parda, branca, indígena ou amarela.
        </p>
    </div>

    <div>
        <h3 class="font-semibold text-gray-900">Comunidade</h3>
        <p class="mt-2">
            Informação referente a pertencimento a comunidades tradicionais (ribeirinhas, quilombolas,
            indígenas ou extrativistas).
        </p>
    </div>
</div>
